#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	void	*str;

	str = s;
	i = 0;
	while (i < n)
	{
		*(char *)str = c;
		str++;
		i++;
	}
	return (s);
}
