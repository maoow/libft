#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (!s)
		return (NULL);
	while (*(char *)s != c && *(char *)s)
		s++;
	if (*(char *)s == c)
		return ((char *)s);
	return (NULL);
}
