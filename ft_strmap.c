#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	int		len;
	char	*str;

	if (s)
	{
		i = 0;
		len = 0;
		while (s[len])
			len++;
		if ((str = (char *)malloc(len + 1 * sizeof(*str))) == NULL)
			return (NULL);
		while (s[i])
		{
			str[i] = (*f)(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
